def read_country(name):
    for country in data:
        if country["name"].lower() == name.lower():
            return country
    return "País no encontrado"

# Función para actualizar las tarjetas amarillas de un país
def update_country(name, tarjetas_amarillas):
    for country in data:
        if country["name"].lower() == name.lower():
            country["mundial_tarjetas_amarillas"] = tarjetas_amarillas
            print(f"Tarjetas amarillas de {name} actualizadas con éxito!")
            return
    print("País no encontrado.")

